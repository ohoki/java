public class StringApp {

	public static void main(java.lang.String[] args) {
		
		//Character VS String
		System.out.println("Hello World"); //String(문자열)- 문자의 집합체- 큰 따음표("")로 표시한다
		System.out.println('H'); //Character(문자)- 문자 한개- 작은 따음표('')로 표시한다
		
		//new line
		System.out.println("Hello \nWorld"); //줄바꿈
		
		//escape
		System.out.println("Hello \"World\""); //Hello "World"
	}
}
